import com.voximplant.apiclient.util.StringHelper;
import com.voximplant.apiclient.util.Alignable;

/**
* Received when a caller ID is expired.
*/
public class ExpiredCallerIDCallback implements Alignable {

    private String[] callerids;

    /**
    * The list of the expired Caller IDs
    */
    public String[] getCallerids() {
        return this.callerids;
    }

    public boolean hasCallerids() {
        return this.callerids != null;
    }

    public String toString(int alignment) {
        char[] preAligned = new char[alignment - 1];
        char[] aligned = new char[alignment];
        Arrays.fill(preAligned, '\t');
        Arrays.fill(aligned, '\t');
        StringBuilder sb = new StringBuilder()
            .append(preAligned)
            .append('{')
            .append(System.lineSeparator());
        if (callerids != null) {
            sb.append(aligned)
                .append("\"string\": ")
                .append(StringHelper.arrayToString(callerids, alignment + 1))
                .append(System.lineSeparator());
        }
        return sb.append(preAligned).append('}').append(',').toString();
    }

    @Override
    public String toString() {
        return toString(1);
    }}